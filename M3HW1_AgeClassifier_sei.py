# get user entries
userAge = int(input("Enter your Age:"))

if userAge <= 1:
    print("\nYou are an infant")
elif userAge < 13:
    print("\nYou are a child")
elif userAge < 20:
    print("\nYou are a teenager")
elif userAge >= 20:
    print("\nYou are an adult")

        

    
